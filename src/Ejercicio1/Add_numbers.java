package Ejercicio1;

public class Add_numbers {

    private int number_1;
    private int number_2;

    public Add_numbers(int number_1, int number_2){
        this.number_1 = number_1;
        this.number_2 = number_2;
    }

    public int add_result(){
        return number_1 + number_2;
    }
}
